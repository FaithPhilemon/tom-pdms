<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Security_question extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model('Security_question_model');
        $this->load->library('form_validation');
    }

    public function index()
    {
        $q = urldecode($this->input->get('q', TRUE));
        $start = intval($this->input->get('start'));
        
        if ($q <> '') {
            $config['base_url'] = base_url() . 'security_question/index.html?q=' . urlencode($q);
            $config['first_url'] = base_url() . 'security_question/index.html?q=' . urlencode($q);
        } else {
            $config['base_url'] = base_url() . 'security_question/index.html';
            $config['first_url'] = base_url() . 'security_question/index.html';
        }

        $config['per_page'] = 10;
        $config['page_query_string'] = TRUE;
        $config['total_rows'] = $this->Security_question_model->total_rows($q);
        $security_question = $this->Security_question_model->get_limit_data($config['per_page'], $start, $q);

        $this->load->library('pagination');
        $this->pagination->initialize($config);

        $data = array(
            'security_question_data' => $security_question,
            'q' => $q,
            'pagination' => $this->pagination->create_links(),
            'total_rows' => $config['total_rows'],
            'start' => $start,
        );
        $this->load->view('security_question/security_question_list', $data);
    }

    public function read($id) 
    {
        $row = $this->Security_question_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id' => $row->id,
		'question' => $row->question,
	    );
            $this->load->view('security_question/security_question_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('security_question'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('security_question/create_action'),
	    'id' => set_value('id'),
	    'question' => set_value('question'),
	);
        $this->load->view('security_question/security_question_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'question' => $this->input->post('question',TRUE),
	    );

            $this->Security_question_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('security_question'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Security_question_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('security_question/update_action'),
		'id' => set_value('id', $row->id),
		'question' => set_value('question', $row->question),
	    );
            $this->load->view('security_question/security_question_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('security_question'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
		'question' => $this->input->post('question',TRUE),
	    );

            $this->Security_question_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('security_question'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Security_question_model->get_by_id($id);

        if ($row) {
            $this->Security_question_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('security_question'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('security_question'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('question', 'question', 'trim|required');

	$this->form_validation->set_rules('id', 'id', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

